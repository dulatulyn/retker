from __future__ import annotations

from .tools import TOOLS


def tools_description() -> str:
    lines = []
    for name, t in TOOLS.items():
        params = ", ".join(f"{k} ({v})" for k, v in t["params"].items()) or "без параметров"
        lines.append(f"- {name}: {t['description']} Параметры: {params}.")
    return "\n".join(lines)


